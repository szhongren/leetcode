    def findRelativeRanksRecur(self, nums, place):
        if len(nums) == 0:
            return []
        max_score = -1
        max_score_i = -1
        for i, v in enumerate(nums):
            if v > max_score:
                max_score = v
                max_score_i = i
        res = self.findRelativeRanksRecur(
            nums[:max_score_i] + nums[max_score_i + 1:],
            place + 1)
        res.insert(max_score_i, self.get_medal(place))
        return res

    def get_medal(self, place):
        if place == 1:
            return "Gold Medal"
        elif place == 2:
            return "Silver Medal"
        elif place == 3:
            return "Bronze Medal"
        else:
            return str(place)

